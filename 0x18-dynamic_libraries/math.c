#include <stdio.h>

/**
 * add - adds two integers.
 * @a: interger value
 * @b: integer value.
 * Return: integer
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * substract - substracts values.
 * @a: type int argument
 * @b: type integer.
 * Return: integer
 */

int subtract(int a, int b)
{
	return (a - b);
}
